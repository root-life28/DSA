#include <bits/stdc++.h>
using namespace std;

bool solve(vector<vector<char>> &vr)
{

    vector<vector<vector<int>>> temp(3, vector<vector<int>>(10, vector<int>(10, 0)));

    for (int i = 0; i < 9; i++)
    {
        for (int j = 0; j < 9; j++)
        {

            if (vr[i][j] != '.')
            {
                int k = vr[i][j] - '0';
                temp[0][i][k]++;
                temp[1][j][k]++;
                temp[2][(3 * (i / 3)) + (j / 3)][k]++;
                if (temp[0][i][k] > 1 || temp[1][j][k] > 1 || temp[2][(3 * (i / 3)) + (j / 3)][k] > 1)
                    return false;
            }
        }
    }

    return true;
}
